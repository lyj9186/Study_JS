// calculator 만들기 복습
// +, -, /, *
//  add, minus, div, multi 

const calculator = {
    add: function(a, b){
        console.log(a + b);
    },
    minus: function(a, b){
        console.log(a - b);
    },
    div: function(a, b){
        console.log(a / b);
    },
    multi: function(a, b){
        console.log(a * b);
    },    
}

calculator.add(8, 2);
calculator.minus(8, 2);
calculator.div(8, 2);
calculator.multi(8, 2);



